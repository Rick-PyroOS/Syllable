RadioMenuItem::RadioMenuItem(const os::String& cLabel,os::Message* msg) : MenuItem(cLabel,msg)
{
	if( s_pcBitmap == NULL )
	{
		s_pcBitmap = new Bitmap( 16, 32, CS_RGB32, Bitmap::SHARE_FRAMEBUFFER );
		memcpy( s_pcBitmap->LockRaster(), g_anImage, 16 * 32 * 4 );
	}
		
	m_bRadio = false;
	m_Highlighted = false;
	m_Enabled = true;
	//m_bIsSelectable = true;

}

void RadioMenuItem::Draw()
{
	Menu *pcMenu = GetSuperMenu();

	if( pcMenu == NULL )
	{
		return;
	}

	const String& cLabel = GetLabel();

	Rect cFrame = GetFrame();

	font_height sHeight;

	pcMenu->GetFontHeight( &sHeight );

	if( m_Highlighted )
	{
		pcMenu->SetFgColor( get_default_color( COL_SEL_MENU_BACKGROUND ) );
	}
	else
	{
		pcMenu->SetFgColor( get_default_color( COL_MENU_BACKGROUND ) );
	}

	pcMenu->FillRect( GetFrame() );

	if( m_Highlighted )
	{
		pcMenu->SetFgColor( get_default_color( COL_SEL_MENU_TEXT ) );
		pcMenu->SetBgColor( get_default_color( COL_SEL_MENU_BACKGROUND ) );
	}
	else
	{
		pcMenu->SetFgColor( get_default_color( COL_MENU_TEXT ) );
		pcMenu->SetBgColor( get_default_color( COL_MENU_BACKGROUND ) );
	}

	float vCharHeight = sHeight.ascender + sHeight.descender + sHeight.line_gap;
	float y = cFrame.top + ( cFrame.Height() + 1.0f ) / 2 - vCharHeight / 2 + sHeight.ascender + sHeight.line_gap * 0.5f;

	pcMenu->DrawString( Point( cFrame.left + 12, y ), cLabel );

	pcMenu->SetDrawingMode( DM_OVER );
	if(m_bRadio)
	{
		pcMenu->DrawBitmap( s_pcBitmap, Rect( 2, 0, 13.0f, 13.0f ), Rect( 0, cFrame.top, 11.0f, cFrame.top+13.0f ) );
	}
	else
	{
		pcMenu->DrawBitmap( s_pcBitmap, Rect( 2, 16.0f, 13.0f, 29.0f ), Rect( 0, cFrame.top, 11.0f, cFrame.top+13.0f ) );
	}	
	pcMenu->SetDrawingMode( DM_COPY );
}

Point RadioMenuItem::GetContentSize() const
{
	Menu *pcMenu = GetSuperMenu();

	if( pcMenu != NULL )
	{
		font_height sHeight;

		pcMenu->GetFontHeight( &sHeight );

		return ( Point( pcMenu->GetStringWidth( GetLabel() ) + 14, sHeight.ascender + sHeight.descender + sHeight.line_gap ) );
	}

	return ( Point( 0.0f, 0.0f ) );
}

bool RadioMenuItem::GetRadio()
{
	return m_bRadio;
}

void RadioMenuItem::SetRadio(bool b)
{
	m_bRadio = b;
}

void RadioMenuItem::SetHighlighted( bool bHighlighted )
{
	m_Highlighted = bHighlighted;
	MenuItem::SetHighlighted( bHighlighted );
}


RadioMenu::RadioMenu( Rect cFrame, const String& cName, MenuLayout_e eLayout,uint32 nResizeMask,uint32 nFlags) : os::Menu(cFrame,cName,eLayout,nResizeMask,nFlags)
{
}

void RadioMenu::MouseDown(const os::Point &cPosition, uint32 nButtons)
{
	if( GetBounds().DoIntersect( cPosition ) )
	{
		RadioMenuItem *pcItem = (RadioMenuItem*)FindMarked();
		for( int i = 0; i < GetItemCount(); i++ )
		{
			os::RadioMenuItem* pcSubItem = (RadioMenuItem*) GetItemAt(i);
			if (pcItem != pcSubItem)
				pcSubItem->SetRadio( false );
		}
		pcItem->SetRadio(true);
		Flush();
		Sync();
	}
	Menu::MouseDown(cPosition,nButtons);
}


bool RadioMenu::AddItem(os::RadioMenuItem* pcItem)
{
	return AddItem(pcItem,GetItemCount());
}

bool RadioMenu::AddItem(os::RadioMenuItem* pcItem, int nIndex)
{
	return Menu::AddItem(pcItem,nIndex);
}




